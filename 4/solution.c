#define min(a,b) (a)<(b)?(a):(b)
#define max(a,b) (a)>(b)?(a):(b)

double findMedianSortedArrays(int *a, int len_a, int *b, int len_b){
    if(len_b < len_a){
        return findMedianSortedArrays(b, len_b, a, len_a);
    }
    if(!len_a){
        return len_b % 2 ? b[len_b / 2] : (double)(b[len_b / 2] + b[len_b / 2 - 1]) / 2;
    }
    int min_elements = 0, max_elements = len_a, elements_a = len_a / 2, elements_b;
    while(1){
        elements_b = ((len_a + len_b) / 2) - elements_a;
        if(elements_b < len_b && elements_a > 0 && a[elements_a - 1] > b[elements_b]){
            max_elements = elements_a - 1;
        }else if(elements_a < len_a && elements_b > 0 && b[elements_b - 1] > a[elements_a]){
            min_elements = elements_a + 1;
        }else{
            break;
        }
        elements_a = (max_elements + min_elements) / 2;
    }
    if((len_a + len_b) % 2){
        return elements_a < len_a ? elements_b < len_b ? min(a[elements_a], b[elements_b]) : a[elements_a] : b[elements_b];
    }
    double i = elements_a < len_a ? elements_b < len_b ? min(a[elements_a], b[elements_b]) : a[elements_a] : b[elements_b];
    double j = elements_a - 1 >= 0 ? elements_b - 1 >= 0 ? max(a[elements_a - 1], b[elements_b - 1]) : a[elements_a - 1] : b[elements_b - 1];
    return (i + j) / 2;
}

